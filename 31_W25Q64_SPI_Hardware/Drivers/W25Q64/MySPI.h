#ifndef __MYSPI_H
#define __MYSPI_H

void MySPI_Init(void);
void MySPI_Start(void);
void MySPI_Stop(void);
uint8_t MySPI_SwapByte(uint8_t ByteSend);

#endif

/*
这就是封装（encapsulation）的思想。

  具体体现在两点：
                                                                                                                          1. 隐藏内部实现：MySPI_W_SS、MySPI_W_SCK、MySPI_W_MOSI 这些操作 GPIO 引脚的函数只在 .c 文件内部使用，不在 .h
  MySPI.h中声明，外部文件无法直接调用它们。                                                                                      2. 暴露简洁接口：外部只需要关心 4 个函数——初始化、开始、停止、交换字节。调用者不需要知道底层用的是 PA4 做片选、PA5
  做时钟、PA7 做 MOSI，也不需要知道是软件 SPI 还是硬件 SPI。

  严格说这更准确的说法是封装而不是解耦。两者的区别：

  - 封装：隐藏内部细节，只暴露必要接口（这里就是这个做法）
  - 解耦：降低模块之间的依赖关系，比如如果将来把软件 SPI 换成硬件 SPI，只要接口不变（还是 MySPI_Start、MySPI_SwapByte
  等），上层代码完全不用改——这体现了解耦

  所以这个设计同时体现了封装和解耦，只是当前这一步（.h 只公开必要函数）主要体现的是封装，为解耦打下了基础。
*/

/*
解耦就是只需要改底层代码就行了是吗，不需要管应用层。即宏定义#define就是典型的解耦
 对，你理解的方向是对的，但可以再精确一点。

  解耦的核心是：模块之间通过接口交互，而不是通过实现细节交互。这样改一个模块时，另一个模块不需要跟着改。
  #define 确实是一个简单有效的解耦手段，但不是解耦的全部。常见的解耦方式对比：
  ┌────────────┬───────────────────────────┬──────────────────────────────────┐
  │    方式    │           例子            │               效果               │
  ├────────────┼───────────────────────────┼──────────────────────────────────┤
  │ 宏定义     │ #define SS_PIN GPIO_PIN_4 │ 改引脚只改一处，不用全文搜索替换 │
  ├────────────┼───────────────────────────┼──────────────────────────────────┤
  │ 头文件接口 │ .h 只公开 MySPI_SwapByte  │ 上层只调接口，底层随便换         │
  ├────────────┼───────────────────────────┼──────────────────────────────────┤
  │ 回调函数   │ 注册回调，不直接调用      │ 调用关系反转，双方不依赖         │
  └────────────┴───────────────────────────┴──────────────────────────────────┘

  #define 更准确地说是消除硬编码/魔法数字，它是解耦的一种具体手段。真正的解耦是一个设计思想，核心就一句话：

  ▎ 依赖接口，不依赖实现。

  你项目里 MySPI.h 只暴露 4 个函数就是这个思想的体现——将来换成硬件 SPI，上层代码一行不用改。
*/